function makeGeoJSON(currency) {
  const features = [];
  for (const cc of currency.countryCodes) {
    const bounds = COUNTRY_BOUNDS[cc];
    if (!bounds) continue;
    const [minLon, minLat, maxLon, maxLat] = bounds;
    // Find the country name from the countries array by matching the country code
    features.push({
      type: "Feature",
      properties: {
        code: currency.code,
        name: currency.name,
        country: cc,
        region: currency.region,
      },
      geometry: {
        type: "Polygon",
        coordinates: [
          [
            [minLon, minLat],
            [minLon, maxLat],
            [maxLon, maxLat],
            [maxLon, minLat],
            [minLon, minLat],
          ],
        ],
      },
    });
  }
  return { type: "FeatureCollection", features };
}

function makeDataFile(currency) {
  return {
    code: currency.code,
    name: currency.name,
    isoCode: currency.isoCode,
    symbol: currency.symbol,
    region: currency.region,
    centralBank: currency.centralBank,
    exchangeRateEra: currency.exchangeRateEra,
    countries: currency.countries,
    countryCount: currency.countries.length,
    description: currency.description,
    historicalNotes: currency.historicalNotes,
    geojson: `${currency.code}.geojson`,
  };
}

let count = 0;
for (const currency of CURRENCIES) {
  const dataFile = join(DATA_DIR, `${currency.code}.json`);
  const geoFile = join(GEO_DIR, `${currency.code}.geojson`);

  writeFileSync(dataFile, JSON.stringify(makeDataFile(currency), null, 2) + "\n");
  writeFileSync(geoFile, JSON.stringify(makeGeoJSON(currency)) + "\n");
  count++;
}

console.log(`Generated ${count} currency data files and GeoJSON files.`);
