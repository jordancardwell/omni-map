for (const country of COUNTRIES) {
  const dataFile = join(DATA_DIR, `${country.code}.json`);
  const geoFile = join(GEO_DIR, `${country.code}.geojson`);

  writeFileSync(dataFile, JSON.stringify(makeDataFile(country), null, 2) + "\n");
  writeFileSync(geoFile, JSON.stringify(makeGeoJSON(country)) + "\n");
  count++;
}

