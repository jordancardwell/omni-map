function makeGeoJSON(country) {
  const bounds = COUNTRY_BOUNDS[country.countryCode];
  if (!bounds) return { type: "FeatureCollection", features: [] };
  const [minLon, minLat, maxLon, maxLat] = bounds;
  return {
    type: "FeatureCollection",
    features: [
      {
        type: "Feature",
        properties: {
          code: country.code,
          name: country.name,
          country: country.countryCode,
          voltageStandard: country.voltageStandard,
        },
        geometry: {
          type: "Polygon",
          coordinates: [
            [
              [minLon, minLat],
              [minLon, maxLat],
              [maxLon, maxLat],
              [maxLon, minLat],
              [minLon, minLat],
            ],
          ],
        },
      },
    ],
  };
}

function makeDataFile(country) {
  const plugDescriptions = country.plugTypes.map((t) => PLUG_DESCRIPTIONS[t] || `Type ${t}`);
  return {
    code: country.code,
    name: country.name,
    voltage: country.voltage,
    frequency: country.frequency,
    plugTypes: country.plugTypes,
    plugTypeCount: country.plugTypes.length,
    plugDescriptions,
    voltageStandard: country.voltageStandard,
    adapterNotes: country.adapterNotes,
    geojson: `${country.code}.geojson`,
  };
}

