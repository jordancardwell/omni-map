// ─── GENERATE FILES ─────────────────────────────────────────────────────

let totalLaneFeatures = 0;
let totalPortFeatures = 0;

// Generate shipping corridor files
for (const corridor of shippingCorridors) {
  const features = corridor.lanes.map((coords) => ({
    type: "Feature",
    properties: {
      code: corridor.code,
      name: corridor.name,
      featureType: "shipping-lane",
      classification: corridor.classification,
    },
    geometry: {
      type: "LineString",
      coordinates: coords,
    },
  }));

  totalLaneFeatures += features.length;

  const geojson = { type: "FeatureCollection", features };
  const data = {
    code: corridor.code,
    name: corridor.name,
    classification: corridor.classification,
    itemCategory: `${corridor.classification} Lane`,
    description: corridor.description,
    geojson: `${corridor.code}.geojson`,
  };

  writeFileSync(join(DATA_DIR, `${corridor.code}.json`), JSON.stringify(data, null, 2));
  writeFileSync(join(GEO_DIR, `${corridor.code}.geojson`), JSON.stringify(geojson));
}

// Generate port files
for (const port of ports) {
  const feature = {
    type: "Feature",
    properties: {
      code: port.code,
      name: port.name,
      featureType: "port",
      portType: port.portType,
      country: port.country,
    },
    geometry: {
      type: "Point",
      coordinates: [port.lon, port.lat],
    },
  };

  totalPortFeatures++;

  const geojson = { type: "FeatureCollection", features: [feature] };
  const data = {
    code: port.code,
    name: port.name,
    country: port.country,
    portType: port.portType,
    cargoVolumeMTons: port.cargoVolumeMTons,
    classification: port.portType,
    itemCategory: port.portType,
    keyRoutes: port.keyRoutes,
    description: `${port.name} in ${port.country}. Annual cargo throughput: ~${port.cargoVolumeMTons} million tons. Type: ${port.portType}.`,
    geojson: `${port.code}.geojson`,
  };

  writeFileSync(join(DATA_DIR, `${port.code}.json`), JSON.stringify(data, null, 2));
  writeFileSync(join(GEO_DIR, `${port.code}.geojson`), JSON.stringify(geojson));
}

